#include <iostream>
using namespace std;
int main()
{
    int input;
    cout << "Please enter an integer:";
    cin >> input;
    if (input < 0)
        {
            cout << "ERROR : Number is negative" << '\n';
        }
    else 
       {
            if(input%2==0)
            {
                cout << "Number is even" << '\n';
            }
            if(input%5==0)
            {
                cout << "Number is divisible by 5" << '\n';
            }
        }
}
